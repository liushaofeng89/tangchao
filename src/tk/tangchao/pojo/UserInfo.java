package tk.tangchao.pojo;

//用户信息

public class UserInfo {
	private String userId;
	private String userName;
	private String userRole;
	private String userType;
	private String userSex;
	private String userJoinTime;
	private String userLeavTime;
	private String userMail;
	private String userLastTime;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserRole() {
		return userRole;
	}
	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}
	public String getUserSex() {
		return userSex;
	}
	public void setUserSex(String userSex) {
		this.userSex = userSex;
	}
	public String getUserJoinTime() {
		return userJoinTime;
	}
	public void setUserJoinTime(String userJoinTime) {
		this.userJoinTime = userJoinTime;
	}
	public String getUserLeavTime() {
		return userLeavTime;
	}
	public void setUserLeavTime(String userLeavTime) {
		this.userLeavTime = userLeavTime;
	}
	public String getUserMail() {
		return userMail;
	}
	public void setUserMail(String userMail) {
		this.userMail = userMail;
	}
	public String getUserLastTime() {
		return userLastTime;
	}
	public void setUserLastTime(String userLastTime) {
		this.userLastTime = userLastTime;
	}
	public String getUserType() {
		return userType;
	}
	public void setUserType(String userType) {
		this.userType = userType;
	}
	
	
	
	
}
