package tk.tangchao.pojo;

//业务信息

public class BusinessInfo {
	private int businessId;
	private String businessName;
	private String businessPrice;
	private String businessAddTime;
	public int getBusinessId() {
		return businessId;
	}
	public void setBusinessId(int businessId) {
		this.businessId = businessId;
	}
	public String getBusinessName() {
		return businessName;
	}
	public void setBusinessName(String businessName) {
		this.businessName = businessName;
	}
	public String getBusinessPrice() {
		return businessPrice;
	}
	public void setBusinessPrice(String businessPrice) {
		this.businessPrice = businessPrice;
	}
	public String getBusinessAddTime() {
		return businessAddTime;
	}
	public void setBusinessAddTime(String businessAddTime) {
		this.businessAddTime = businessAddTime;
	}
	

}
